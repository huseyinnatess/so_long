int	check_file(char *file)
{
	int	i;

	i = -1;
	while (file[++i])
	{
		if (file[i] == '.')
		{
			if (file[i + 1] == 'b' && file[i + 2] == 'e' && file[i + 3] == 'r')
				return (0);
		}
	}
	return (1);
}
